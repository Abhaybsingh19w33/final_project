String cleanText(String sentence) {
  final filter = RegExp("[^a-z]+");
  sentence = sentence.toLowerCase();

  sentence = sentence.replaceAll(filter, ' ');

  // taking only alphabets
  List<String> listOfWords = sentence.split(' ');

  List<String> listOfFilteredWords = [];
  for (var element in listOfWords) {
    if (element.length > 1) {
      listOfFilteredWords.add(element);
    }
  }

  sentence = "";

  for (String element in listOfFilteredWords) {
    if (element.length > 1) {
      sentence = sentence + element + " ";
    }
  }

  if (sentence.length > 1) {
    return sentence;
  }
  return "";
}
